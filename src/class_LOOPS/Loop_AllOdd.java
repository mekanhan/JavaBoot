package class_LOOPS;

public class Loop_AllOdd {
    public static void main(String[] args) {
        for (int i=0; i<=99; i++)
        {
            System.out.print(i=i+1);
            System.out.print(",");
        }
        System.out.println("");
        for(int i=1;i<=99;i++)
        {
            System.out.print(i=i+1);
            System.out.print(",");
        }
    }
}
