package Class_Feb19;

public class Print_Triangle_For_Loop {
    public static void main(String[] args){


        for(int i=1; i<10; i++){
            System.out.println();
            for (int j=1; j<2; j++){
                System.out.print("*");

            }
            while (i<10){
                System.out.printf("*");
                i++;
            }
        }

    }
}
