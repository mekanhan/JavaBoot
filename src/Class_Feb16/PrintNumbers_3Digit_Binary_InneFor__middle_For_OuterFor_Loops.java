package class_Feb16;

public class PrintNumbers_3Digit_Binary_InneFor__middle_For_OuterFor_Loops {
    public static void main(String[] args){
    /* Print all binary numbers 0-7 using loop

    */

        for (int i=0; i<2; i++){
            for (int j=0; j<2; j++){
                for (int k=0; k<2; k++){
                    System.out.println(i+""+j+""+k);
                }
            }
        }
    }
}
