package class_March7_Review;

public class DataTypes {
    public static void main(String[] args) {

        System.out.println(4/5);    //double
        System.out.println(2.0/3);  //double
        System.out.println((float)(2.0/3)); //float
        System.out.println('a'/3);   // float
        System.out.println('A'/9);  // double
    }
}
