package class_Feb22;

public class GradeSwitch {
    public static void main(String[] args){
        int grade=90;
        switch((int) (grade)){
            case 90:
                System.out.println("A");
            case 80:
                System.out.println("B");
            case 70:
                System.out.println("C");


        }
    }
}
